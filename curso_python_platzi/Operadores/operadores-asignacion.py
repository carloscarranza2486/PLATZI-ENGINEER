# Operadores Asignación

x = 5
y = 3
z = 5

print(x == y) # Si es igual False
print(x != y) # Si es distinto True
print(x > z) # Es mayor a segunda True
print(x < y) # Es menosr False
print(x >= y) # Es mayor o igual true
print(x <= y) # Es menor o igual False

# Operadores lógicos

# primera condición true, segunda. False

print(x > y and y > z) # Fase
print(x > y or y > z) # True

# Negación

v = True
print(not(v))
